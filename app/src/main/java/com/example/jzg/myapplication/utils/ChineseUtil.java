	/**
	 * 是否包含汉字字符
	 * 
	 * @param cnStr
	 *            指定字符串
	 * @return true:是 false:否
	 */
	public static boolean isContainChineseCharacter(String cnStr)
	{
		if (TextUtils.isEmpty(cnStr))
		{
			return false;
		}
		char[] chCnArs = cnStr.toCharArray();
		boolean bIsContainChineseCharacter = false;
		for (int i = 0; i < chCnArs.length; i++)
		{
			byte[] byteCns = ("" + chCnArs[i]).getBytes();
			if (byteCns.length == 2)
			{
				int[] iInts = new int[2];
				iInts[0] = byteCns[0] & 0xff;
				iInts[1] = byteCns[1] & 0xff;
				if (iInts[0] >= 0x81 && iInts[0] <= 0xFE && iInts[1] >= 0x40
						&& iInts[1] <= 0xFE)
				{
					bIsContainChineseCharacter = true;
					break;
				}
			}
		}
		return bIsContainChineseCharacter;
	}

